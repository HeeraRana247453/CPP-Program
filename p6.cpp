//To extract the multiple values at the same time.
#include<iostream>
using namespace std;

int main()
{
    int a,b,c,d;
    cout<<"Enter the value of the a, b, c and d: ";
    cin>>a>>b>>c>>d;

    cout<<"The value of the a: "<<a<<endl;
    cout<<"The value of the b: "<<b<<endl;
    cout<<"The value of the c: "<<c<<endl;

    return 0;
}